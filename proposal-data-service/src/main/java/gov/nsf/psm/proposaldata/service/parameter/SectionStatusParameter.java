package gov.nsf.psm.proposaldata.service.parameter;

public class SectionStatusParameter {
    
    String propPrepId = null;
    String propPrepRevnId = null;
    String sectionCode = null;
    
    public String getPropPrepId() {
        return propPrepId;
    }
    public void setPropPrepId(String propPrepId) {
        this.propPrepId = propPrepId;
    }
    public String getPropPrepRevnId() {
        return propPrepRevnId;
    }
    public void setPropPrepRevnId(String propPrepRevnId) {
        this.propPrepRevnId = propPrepRevnId;
    }
    public String getSectionCode() {
        return sectionCode;
    }
    public void setSectionCode(String sectionCode) {
        this.sectionCode = sectionCode;
    }

}
